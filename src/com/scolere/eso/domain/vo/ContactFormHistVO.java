package com.scolere.eso.domain.vo;

public class ContactFormHistVO {
	
	private int formSrNo;
	private String name;
	private String email;
	private int inquiry;
	private String lastUpdtTm;
	
	public int getFormSrNo() {
		return formSrNo;
	}
	public void setFormSrNo(int formSrNo) {
		this.formSrNo = formSrNo;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getInquiry() {
		return inquiry;
	}
	public void setInquiry(int inquiry) {
		this.inquiry = inquiry;
	}
	public String getLastUpdtTm() {
		return lastUpdtTm;
	}
	public void setLastUpdtTm(String lastUpdtTm) {
		this.lastUpdtTm = lastUpdtTm;
	}
	


}
